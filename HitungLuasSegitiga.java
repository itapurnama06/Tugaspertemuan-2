public class HitungLuasSegitiga {
     public static void main(String[] args) {
        // Nilai alas
        double alas = 40; // dalam satuan cm
        
        // Nilai tinggi
        double tinggi = 60; // dalam satuan cm
        
        // Menghitung luas segitiga
        double luas = (alas * tinggi) / 2;
        
        // Menampilkan hasil
        System.out.println("Luas segitiga dengan alas 40 cm dan tinggi 60 cm adalah: " + luas + " cm^2");
    }

}
